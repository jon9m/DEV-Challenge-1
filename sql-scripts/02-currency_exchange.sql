  `currency` varchar(45) DEFAULT NULL,
  `date` varchar(45) DEFAULT NULL,
  `time` time DEFAULT NULL,
  `price` float (20) DEFAULT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=6 DEFAULT CHARSET=latin1;

